public class Herec {
    private String jmeno;
    private int rok_narozeni;
    Herec(String jmeno, int rok_narozeni) {
        this.jmeno = jmeno;
        this.rok_narozeni = rok_narozeni;
    }
    public String getJmeno() {
        return jmeno;
    }
    public int getRok_narozeni() {
        return rok_narozeni;
    }

}
